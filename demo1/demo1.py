# -*- coding: utf-8 -*
# python基本数据类型
print(type(1))  #int
print(type(1.234)) #float
print(type('hbb')) #str
print(type([]))  #list
print(type({}))  #dict

print(type(1+1))  #int
print(type(1+1.1)) #float
print(type(1+1.0))  #float


print(type(1*2))  #int
print(type(1*2.0)) #float
# 触发想要得到int，必须用  //
print(type(4/2))  #float
print(type(4/2.0))  #float
print(type(4//2))  #int
print(type(4//2.0))  #float